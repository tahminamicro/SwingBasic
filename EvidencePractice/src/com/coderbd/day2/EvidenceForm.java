    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        // TODO add your handling code here:
        if(txtNAme.getText().trim().length()<3){
            JOptionPane.showMessageDialog(null, "At least 3 charaecters");
        }else if(Integer.parseInt(txtId.getText())<2){
        JOptionPane.showMessageDialog(null, "At least 2 numbers");
        }else if(!rMAle.isSelected() && !rFemale.isSelected()){
        JOptionPane.showMessageDialog(null, "Select your gender");
        }else if(!chkJava.isSelected() && !chkJS.isSelected() && !chkHTML.isSelected()){
        JOptionPane.showMessageDialog(null, "Select your Courses");
        }else if(cmbRound.getItemAt(cmbRound.getSelectedIndex()).equalsIgnoreCase("Select a round")){
        JOptionPane.showMessageDialog(null, "Select your Round");
        }
        
        else{
        String name = txtNAme.getText();
        System.out.println(" Name " + name);
        
        int id = Integer.parseInt(txtId.getText());
        System.out.println(" ID " + id);
        
        String gender=" ";
        if(rMAle.isSelected()){
        gender = rMAle.getText();
        }
        
         if(rFemale.isSelected()){
        gender = rFemale.getText();
        }
         
         System.out.println("Gender " + gender);
         
         String course= " ";
         
         if(chkJava.isSelected()){
         course += chkJava.getText();
         }
         
          if(chkJS.isSelected()){
         course += chkJS.getText();
         }
          
           if(chkHTML.isSelected()){
         course += chkHTML.getText();
         }
           
           System.out.println("Course " + course);
           
           String round = cmbRound.getItemAt(cmbRound.getSelectedIndex());
           System.out.println("Round: " + round);
           
           String comment=txtMsg.getText();
           System.out.println("Comment " + comment);
           
           DefaultTableModel model = (DefaultTableModel)tblDisplay.getModel();
           Object[] row = new Object[6];
           row[0]= name;
           row[1]= id;
           row[2]= gender;
           row[3]= course;
           row[4]= round;
           row[5]= comment;
           model.addRow(row);
           
           Evidence s = new Evidence(name, id, gender, course, round, comment);
           
           List<Evidence> list = new ArrayList<>();
           list.add(s);
           
            try {
                Utility.writeToFile("Tahmina", list);
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnADDActionPerformed

